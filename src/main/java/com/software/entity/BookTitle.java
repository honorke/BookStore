package com.software.entity;

public class BookTitle {
	int bookId;
	int bookNo;
	String bookName;
	String bookPicture;
	String bookWriter;
	String bookPublishDate;
	Double bookNowPrice;
	Double bookPrePrice;
	public int getBookId() {
		return bookId;
	}
	public void setBookId(int bookId) {
		this.bookId = bookId;
	}
	public int getBookNo() {
		return bookNo;
	}
	public void setBookNo(int bookNo) {
		this.bookNo = bookNo;
	}
	public String getBookName() {
		return bookName;
	}
	public void setBookName(String bookName) {
		this.bookName = bookName;
	}
	public String getBookPicture() {
		return bookPicture;
	}
	public void setBookPicture(String bookPicture) {
		this.bookPicture = bookPicture;
	}
	public String getBookWriter() {
		return bookWriter;
	}
	public void setBookWriter(String bookWriter) {
		this.bookWriter = bookWriter;
	}
	public String getBookPublishDate() {
		return bookPublishDate;
	}
	public void setBookPublishDate(String bookPublishDate) {
		this.bookPublishDate = bookPublishDate;
	}
	public Double getBookNowPrice() {
		return bookNowPrice;
	}
	public void setBookNowPrice(Double bookNowPrice) {
		this.bookNowPrice = bookNowPrice;
	}
	public Double getBookPrePrice() {
		return bookPrePrice;
	}
	public void setBookPrePrice(Double bookPrePrice) {
		this.bookPrePrice = bookPrePrice;
	}
	
	
	

}
